<p class="card-text">Because you are an Organizer, you are interested in keeping orderly systems in place. You will often enjoy office and administrative work, especially working with systems, records, and files. You like to maintain structure and enforce the rules, and want to work in a well organized and predictable environment.</p>

<p class="card-text">Organizers are detail-oriented and responsible people with an appreciation for structure and order. They typically take things literally and have little tolerance for sloppiness or people who try to bend the rules. They are cautious and careful and typically dislike taking risks.</p>

<p class="card-text">Organizers like their work best when they can carefully systematize processes to work toward well-defined goals. As an Organizer, your primary career goal will be to find a job where you can focus on the details and create reliable systems to produce  predictable, precise results.</p>

