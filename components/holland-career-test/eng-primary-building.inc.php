<p class="card-text">Because you are a Builder, you will prefer a career that allows you to use and hone your hands-on
    skills. You enjoy learning to construct, craft, repair, maintain, and tinker. You like creating practical things and
    fixing necessary systems. </p>

<p class="card-text">Builders tend to be pragmatic, down-to-earth people who take a “just do it” approach to life.
    Builders often dislike having to explain or discuss their work with others, preferring instead to jump in and simply
    get it done.</p>

<p class="card-text">Builders like their work best when they can see a real, physical result of their efforts. As a
    Builder, your primary career goal will be to discover a job where you can use your physical or mechanical skills to
    take useful, observable action on the world around you.</p>

